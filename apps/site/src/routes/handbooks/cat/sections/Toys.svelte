<Section title="Cat toys and how to use them">
	<p>
		Although cats generally have different play styles than their canine counterparts toys are as
		much a necessity for cats as they are for dogs. Toys help fight boredom and give cats an outlet
		for their instinctive prey-chasing behaviors. When you are the one moving the toy around while
		your cat fishes for it, chases after it, or jumps in pursuit of it, playtime becomes a bonding
		experience for you and your cat.
	</p>
	<SubSection title={`"Safe" toys`}>
		<p>
			Our mothers always told us “no playing ball in the house,” but cats can usually participate in
			that forbidden exercise without knocking down a vase or a lamp (and being grounded for two
			weeks). Still, there are plenty of factors that may contribute to the safety of the toy
			they’re batting around.
		</p>
		<p>
			Many of those factors are completely dependent upon your cat’s size, activity level, and
			preferences. Another factor to be considered is the environment in which your cat spends her
			time. Although we can’t guarantee your cat’s enthusiasm or her safety with any specific toy,
			we can offer the following guidelines.
		</p>
	</SubSection>
	<SubSection title="Be cautious">
		<p>
			The things that are usually most attractive to cats are often the very things that are the
			most dangerous. Cat-proof your home by removing string, ribbon, yarn, rubber bands, plastic
			milk jug rings, paper clips, pins, needles, dental floss, and anything else that could be
			ingested. All of these items are dangerous, no matter how cute your cat may appear when she’s
			playing with them.
		</p>
		<p>
			Avoid or alter any toys that aren’t “cat proof” by removing ribbons, feathers, strings, eyes,
			or other small parts that could be chewed and ingested.
		</p>
		<p>
			Soft toys should be machine washable. Look for stuffed toys that are labeled as safe for
			children less than three years of age and that don’t contain any dangerous fillings. Problem
			fillings include things like nutshells and polystyrene brands. Remember that rigid toys are
			not as attractive to cats.
		</p>
	</SubSection>
	<SubSection title="Active toys">
		<ul class="list-disc pl-6">
			<li>
				Round plastic shower curtain rings, which are fun either as a single ring to bat around,
				hide, or carry, or when linked together and hung in an enticing spot.
			</li>
			<li>Plastic balls with or without bells inside.</li>
			<li>
				Ping-Pong balls and plastic practice golf balls with holes to help cats carry them: try
				putting one in a dry bathtub, as a captive ball is much more fun than one that escapes under
				the sofa. You’ll probably want to remove the balls from the bathtub before bedtime, or you
				may lose some sleep as two o’clock in the morning seems to be a prime time for this game.
			</li>
			<li>
				Paper bass with any handles removed. Paper bags are good for pouncing, hiding, and
				interactive play. Plastic bags are not a good idea as many cats like to chew and ingest the
				plastic.
			</li>
			<li>Sisal-wrapped toys, which are very attractive to cats who tend to ignore soft toys.</li>
			<li>
				Empty cardboard tubes from toilet paper and paper towels make even more fun if you “unwind”
				a little cardboard to get them started.
			</li>
		</ul>
	</SubSection>
	<SubSection title="Comfort toys">
		<ul class="list-disc pl-6">
			<li>
				Soft stuffed animals, which are good for several purposes: for some cats, the stuffed
				animals should be small enough to carry around. For cats who want to wrestle with the toy,
				the stuffed animal should be about the same size as the cat. Toys with legs and a tail seem
				to be especially enticing to cats.
			</li>
			<li>Cardboard boxes, especially those a little too small for your cat to fit into.</li>
		</ul>
	</SubSection>
	<SubSection title="Catnip">
		<ul class="list-disc pl-6">
			<li>
				Catnip-filled soft toys, which cats like to kick, carry and rub: Catnip is not addictive and
				is perfectly safe for cats to roll in, rub in, or eat.
			</li>
			<li>
				Plain catnip can be crushed and sprinkled on the carpet or for easier cleanup on a towel
				placed on the floor. Catnip oils will often stay in the carpet, and although they’re not
				viable to us, your cat will still be able to smell them.
			</li>
			<li>Catnip sprays rarely have enough power to be attractive to cats.</li>
			<li>
				Not all cats are affected by catnip. Some cats may become over stimulated to the point of
				aggressive play and others may become relaxed.
			</li>
			<li>Kittens under six months old seem to be immune to catnip.</li>
		</ul>
	</SubSection>
	<SubSection title="Getting the most out of toys">
		<ul class="list-disc pl-6">
			<li>
				Rotate your cat’s toys weekly by making only a few available at a time. Keep a variety of
				types easily accessible. If your cat has a favorite, like a soft “baby” that she loves to
				cuddle with you may want to leave that one out all the time.
			</li>
			<li>
				Provide toys that offer a variety of uses - at least one toy to carry, one to wrestle with,
				one to roll, and one to “baby.”
			</li>
			<li>
				Hide-and-seek is a fun game for cats. “Found” toys are often much more attractive than a toy
				which is obviously introduced.
			</li>
			<li>
				Many of your cat’s toys should be interactive. Interactive play is very important for your
				cat because he or she needs active “play time” and such play also enhances the bond between
				you and your pet. Cats generally engage in three types of play - “fishing, flying and
				chasing” - and al types are much more engaging for cats when you are part of them.
			</li>
		</ul>
	</SubSection>
</Section>

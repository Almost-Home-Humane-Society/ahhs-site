	<SubSection title="Basic manners">
		<p>
			Having good verbal control of your dog can really help when it comes to juggling her needs and
			the baby’s care. The following are particularly important:
		</p>
		<ul class="list-disc pl-6">
			<li>Sit and down</li>
			<li>
				Sit, wait at doors, and settle. These skills can help your dog learn to control her impulses
				and they’ll prove useful in many situations. For example, you can teach your dog to lie down
				and stay whenever your sit in your nursing chair.
			</li>
			<li>
				Leave it and drop it. These two behaviors can help you teach your dog to leave the baby’s
				things alone.
			</li>
			<li>
				Greet people politely. A jumping dog can be annoying at best (and dangerous at worst) when
				you’re holding the baby.
			</li>
			<li>
				Relax in a crate. If you crate train your dog, you’ll know that she’s safe when you can’t
				supervise her, and she’ll have a cozy place of her own to relax when things get hectic.
			</li>
			<li>Come when called.</li>
		</ul>
		<p>These additional skills can also be helpful:</p>
		<ul class="list-disc pl-6">
			<li>
				Hand targeting. If your dog is nervous or timid, teaching her to target your hand with her
				nose will give her something to do when she’s around the baby, which might make her feel
				more comfortable and confident. After your dog learns how to target your hand, you can even
				teach her to gently touch the baby with her nose!
			</li>
			<li>
				Please go away. Teaching your dog to go away when you ask will enable you to control her
				movements and interactions with your baby. For example, you can use this cue to tell your
				dog to move away from the baby if he’s crawling toward her and she seems uncomfortable. Many
				dogs don’t realize that moving away is an option! If she learns that she can simply walk
				away from the baby when he makes her nervous, she’ll never feel trapped in a stressful
				situation - and she won’t be forced to express her anxiety by growling or snapping. Here’s
				how to teach your dog this invaluable skill:
				<ul class="list-disc pl-6">
					<li>
						Show her a treat, say “go away,” and toss the treat four or five feet away from you.
						Repeat this sequence many times.
					</li>
					<li>
						The next step is to refrain from tossing the treat until your dog starts to move away.
						Say “go away,” and move your arm as though you’re tossing a treat. When your dog moves
						in the direction of your gesture, even if she only takes one step, say “yes!” then
						immediately toss a treat four or five feet away in the direction she started to move.
					</li>
					<li>
						After more repetitions, try waiting until your dog takes several steps away before you
						say “yes!” and toss a treat.
					</li>
				</ul>
			</li>
		</ul>

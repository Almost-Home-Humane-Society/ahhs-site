<Section title="Separation Anxiety">
	<p>
		Many dogs are upset by even the slightest change in routine and, as a result, may become
		anxious. These behaviors are usually worse when left alone and could include barking, howling,
		chewing, urinating in the house, pacing, or even trying to escape.
	</p>
	<SubSection title="What can I do to help?">
		<ul class="list-disc pl-6">
			<li>
				<strong class="font-bold pr-1">Doctor Knows Best:</strong>Always take your pet for a trip to
				the vet first to rule out any medical problems that could be causing the issues.
			</li>
			<li>
				<strong class="font-bold pr-1">Overcome Fear: </strong>Help your dog associate alone time
				with something positive. Food and/or treat dispensing toys (such as Kongs) are a great way
				to keep their minds occupied and make a positive association with time spent alone.
			</li>
			<li>
				<strong class="font-bold pr-1">Dogs Need a Job: </strong>Don’t let your dog become
				“self-employed.” Keep your dog busy and active while you’re home so that her energy is burnt
				up before you leave. Play time, a trip to the dog park, or a long walk are all great ways to
				entertain your pet.
			</li>
			<li>
				<strong class="font-bold pr-1">Prepare for Departure: </strong>Your dog may automatically
				assume that every time you pick up your purse or put on your jacket that you are leaving and
				will start to get upset. Try “preparing to leave,” but simply sit back down and watch some
				television or read a book while dress to leave the house.
			</li>
			<li>
				<strong class="font-bold pr-1">Baby Steps: </strong>Dogs don't overcome anxiety all at once.
				Try leaving your dog alone for very short, non-stress inducing periods of time and then
				gradually increase the amount of time they spend alone.
			</li>
			<li>
				<strong class="font-bold pr-1">Togetherness: </strong>If at all possible, avoid leaving your
				pet alone except for the periods of sensitization. Seek out a pet sitter or bring your pet
				to work if possible. Let them gradually adjust on their own terms to periods of alone time,
				especially if the anxiety is severe.
			</li>
			<li>
				<strong class="font-bold pr-1">Keep Calm and Carry On: </strong>Make sure all greetings and
				goodbyes are done in a similarly calm manner. Don’t make a scene out of leaving or coming
				home.
			</li>
			<li>
				<strong class="font-bold pr-1">No Tough Love: </strong>Do not scold your pet for its anxious
				behavior! Anxiety is an issue that can be overcome and will require patience and persistence
				by the pet parent.
			</li>
		</ul>
	</SubSection>
</Section>

<Section title="Resource guarding">
	<p>
		If your new dog has a history of guarding food or other resources in the home, this means she
		may have frozen up, growled, or even bitten! There is no guarantee that your dog will never
		guard again, but there are ways you can reduce the chances of it occurring. Clicker training,
		for instance, is a good way to reduce any stress your new dog may be feeling. For more
		information, see the “Clicker Training” drop down tab under The Dog Handbook.
	</p>
	<SubSection title="Causes">
		<p>
			Food and other resource guarding can be the result of stress and limited resources. It can
			also be a behavior that she learned in a previous home.
		</p>
	</SubSection>
	<SubSection title="What to do">
		<p>
			Resource guarding may not go away on its own, and if not managed well, may get worse. Some
			guarding, when worked with, can decrease in intensity but may not disappear entirely. To work
			with resource guarding, you can follow these steps:
		</p>
		<ul class="list-disc pl-6">
			<li>
				Take responsibility for items that your dog may find high-value (and is likely to guard) by
				picking them up and keeping them our of reach.
			</li>
			<li>
				Always trade up when you need to retrieve something from your dog. IF she has a toy or item
				she is not supposed to have, bring a few extra-tasty treats to toss to the side so she drops
				the time to eat the treats. This creates an association with your dog that you are not there
				to take things away from her, but to bring her extra good things.
			</li>
			<li>
				If you have seen your dog guard in specific situations, you can then manage those situations
				- such as confining your dog when giving her a bone, or feeding her and then not disturbing
				her until the food is gone.
			</li>
			<li>
				If your dog is showing resource aggression towards other dogs, this is a normal behavior and
				something that can’t be trained out and is best managed. Separate the dogs at feeding time
				or when giving treats/bones/toys; otherwise, avoid giving out these items if that is not
				possible.
			</li>
		</ul>
	</SubSection>
	<SubSection title="What not to do">
		<p>
			Do not punish your dog for guarding during or after the incident. Punishment will only make
			her more likely to guard that item again. Dogs do not understand punishment even after the
			fact, and it can make things worse, potentially leading to aggression.
		</p>
	</SubSection>
	<SubSection title="Realistic expectations">
		<p>
			Some of the things that cause dogs to guard can be difficult to stop or control. For example,
			if your dog guards his good bowl, he still needs to be fed daily, so feeding her in another
			area is a great way to mange this.
		</p>
	</SubSection>
</Section>

	<SubSection title="Location preferences">
		<p>Your cat may have a location preference if:</p>
		<ul class="list-disc pl-6">
			<li>
				He or she always eliminates in quiet, protected places, such as under a desk, beneath a
				staircase, or in a closet.
			</li>
			<li>
				He or she eliminates in an area where the litter box was previously kept or where there are
				urine odors.
			</li>
			<li>She eliminates on a different level of the home from where the litter box is located.</li>
		</ul>
		<div class="flex flex-col gap-1">
			<h5 class="text-base font-bold">What you can do</h5>
			<p>
				Put at least one litter box on every level of your home. (Remember, a properly cleaned
				litter box does not smell).
			</p>
			<p>
				To make the area where he or she has been eliminating less appealing to your cat, cover the
				area with an upside-down carpet runner or aluminum foil, place citrus-scented cotton balls
				over the area, or place water bowls in the area (because cats often don’t like to eliminate
				near where they eat or drink), or put a litter box in the location where your cat has been
				eliminating. When she has consistently used this box for one month, you may gradually move
				it to a more convenient location at the rate of an inch per day.
			</p>

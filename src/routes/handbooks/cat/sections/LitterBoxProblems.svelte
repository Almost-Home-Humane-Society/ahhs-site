				completely every four to five days. If you use scoop able litter, you may not need to change
				the litter as frequently, depending on the number of cats in the household, the size of the
				cats, and the number of litter boxes.
			</p>
			<p>
				If you can smell ht box, then you can be pretty sure it’s offensive to your cat as well. Add
				a new box in a different location and use a different type of litter in the new box. Because
				your cat has decided that her old litter box is unpleasant, you’ll want to make the new one
				different enough that she doesn’t simply apply the old, negative associations to the new
				box.
			</p>
			<p>
				Make sure that the litter box isn’t near an appliance (such as a furnace) that makes noise
				or in an area of the home that your cat doesn’t frequent.
			</p>
			<p>
				If ambushing is a problem, create more than one exit from the litter box, so that if the
				“ambusher” is waiting by one area, your cat always has an escape route.
			</p>
			<p>
				If you have multiple cats, provide one litter box for each cat, plus one extra box in a
				different location.
			</p>

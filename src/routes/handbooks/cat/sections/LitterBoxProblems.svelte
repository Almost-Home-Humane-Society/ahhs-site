				litter that your cat had been suing. If your cat is eliminating on soft surfaces, try using
				a high-quality scoop-able litter.
			</p>
			<p>
				If your cat is eliminating on slick, smoother surfaces, try putting a very thin layer of
				litter at one end of the box, leaving the other end bare, and put the box on a hard floor.
			</p>
			<p>If your cat has a history of being outdoors, add some soil or sod to the litter box.</p>
			<p>
				To discourage your cat from using a certain area, cover the area with an upside-down carpet
				runner or aluminum foil or place citrus-scented cotton balls over the area.

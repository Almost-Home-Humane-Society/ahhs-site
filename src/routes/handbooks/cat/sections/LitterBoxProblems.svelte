	<SubSection title="Surface preferences">
		<p>
			All animals develop preferences for a particular surface on which they like to eliminate.
			These preferences may be established early in life, but they may also change overnight for
			reasons that we don’t always understand. Your cat may have a surface preference if:
		</p>
		<ul class="list-disc pl-6">
			<li>
				He or she consistently eliminates on a particular texture for example, soft-textured
				surfaces such as carpeting, bedding, or clothing, or slick-textured surfaces such as tile,
				cement, bathtubs, or sinks.
			</li>
			<li>
				He or she frequently scratches on this same texture after elimination, even if she
				eliminates in the litter box.
			</li>
			<li>
				He or she is or was previously an outdoor cat and prefers to eliminate on grass or soil.
			</li>
		</ul>
		<div class="flex flex-col gap-1">
			<h5 class="text-base font-bold">What you can do</h5>
			<p>

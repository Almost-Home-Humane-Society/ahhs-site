<Section title="Reducing your cat's fearful behavior">
	<p>
		Flight, flee or freeze. No, it isn’t the latest game show sweeping the nation. Instead, these
		three “f’s” describe the ways the cats usually respond to objects, persons, or situations they
		perceive as a threat.
	</p>
	<p>
		It’s normal for you to want to help and comfort your cat when he or she is frightened. However,
		that isn’t necessarily the best thing to do from your cat’s point of view. It’s normal for a cat
		to feel insecure or frightened in a new environment. Often, your new cat will hide for a day or
		two when you first bring him or her home. Sometimes a traumatic experience such as taking him or
		her for a car ride to the veterinarian or introducing a new animal into the home can disrupt
		their routine and send him or her under the bed for a few days.
	</p>
	<p>
		Each cat has his or her preferred way of dealing with a crisis. You’ll notice that your cat
		probably tends to try one option first, and if that doesn’t work, he or she is forced to try a
		different option. For instance, if your cat is afraid of dogs and a friend brings his dog to
		your home to visit, your cat puffs out their fur to make them-self look big, then hisses and
		spits at the dog. If the dog doesn’t retreat, your cat may flee the situation, find a hiding
		spot, and freeze until he or she deems the situation safe.
	</p>
	<p>Your cat may show the following behaviors when he or she is fearful:</p>
	<ul class="list-disc pl-6">
		<li>Fleeing</li>
		<li>Hiding</li>
		<li>
			Aggression (which includes spitting, hissing, growling, swatting, biting, scratching, puffing
			fur and tail, arching back, swishing tail, and flattening ears)
		</li>
		<li>Loss of control over bladder or bowels</li>
		<li>Freezing</li>
	</ul>
	<p>
		Although some fearful behaviors are acceptable, overly anxious or fearful cats may need help
		from you in the form of training, patience, and love.
	</p>
	<SubSection title="What causes fearful behavior?">
		<p>
			You’ll need to observe your cat closely to determine the trigger for their fearful behavior.
			Keep in mind that just because you know that the person or animal approaching your cat has
			good intentions doesn’t mean your cat shares the same view. The trigger for their fearful
			behavior could be anything. Some common triggers are:
		</p>
		<ul class="list-disc pl-6">
			<li>A particular person</li>
			<li>A stranger</li>
			<li>Another animal</li>
			<li>A child</li>
			<li>Loud noises</li>
		</ul>
	</SubSection>
	<SubSection title="What you can do">
		<p>
			Take the following steps to reduce your cat’s anxiety and help him or her become more
			confident:
		</p>
		<p>
			First, schedule an appointment with your veterinarian for a thorough physical examination to
			rule out any medical reasons for your cat’s fearful behavior. Cats don’t always act sick, even
			when hey are. Any sudden behavior change could mean that your cat is ill. Some common symptoms
			that suggest your cat may be sick are sudden aggressiveness, hiding, and eliminating outside
			the litter box.
		</p>
		<p>
			If your cat is healthy, but hiding, leave the cat alone. He or she will come out when they are
			ready. Forcing them out of their hiding spot will only make them more fearful. Make sure he or
			she has easy access to food, water and their litter box. Clean the litter box and change the
			food and water every day so you know whether he or she is eating and drinking.
		</p>
		<p>Keep any contact with the fear stimulus to a minimum.</p>
		<p>
			Keep your cat’s routine as consistent as possible. Cats feel more confident if they know when
			to expect daily feeding, playing, cuddling and grooming.
		</p>
		<p>Try to desensitize your cat to fear stimulus by following these steps:</p>
		<ul class="list-disc pl-6">
			<li>
				Determine what distance your cat can be from the fear stimulus without responding fearfully
			</li>
			<li>
				Introduce the fear stimulus at this distance while you’re feeding your cat tasty treats and
				praising her.
			</li>
			<li>
				Slowly move the fear stimulus closer as you continue to praise your cat and offer her
				treats.
			</li>
			<li>
				If at any time during this process your cat shows fearful behavior, you’ve proceeded too
				quickly and will need to start over from the beginning. this is the most common mistake
				people make when desensitizing an animal; it can be avoided by working in short session,
				paying careful attention to your cat so that you don’t progress too rapidly for him or her.
			</li>
			<li>
				You may need help with the desensitization process from an animal-behavior specialist.
			</li>
		</ul>
	</SubSection>
	<SubSection title="A not about agression">
		<p>
			If your cat seriously threatens you, another person, or an animal- and the behavior is not an
			isolated incident - you should seek help as soon as possible from an animal behavior
			specialist. Contact your veterinarian or animal shelter for assistance or a referral if you
			need a specialist. To keep everyone safe in the meantime, confine your cat to an area of the
			house where all interactions with her are kept to a minimum and are supervised by a
			responsible person.
		</p>
		<p>
			Cat bites and scratches are serious and can easily become infected. Bites that need to be
			treated by a doctor should be reported to your local animal control agency; your cat may need
			to be quarantined and watch for signs or rabies. If you can’t keep your cat separated from the
			stimulus that triggers her aggressive behavior and you’re unable to work with an animal
			-behavior specialize, you may need to reevaluate the cat’s situation in your home. Remember,
			trying to place an aggressive cat in a new home should be done with extreme caution. The
			safety of your cat and of the other animals and humans he or she encounters should be your
			first consideration.
		</p>
	</SubSection>
	<SubSection title="What not to do">
		<ul class="list-disc pl-6">
			<li>
				Do not punish your cat for his or her fearful behavior. Animals associate punishment with
				what they’re doing at the time they’re punished, so your cat is likely to associate any
				punishment you give her with you. This will only cause her to become fearful of you, and she
				still won’t understand why she’s being punished.
			</li>
			<li>
				Do not force your cat to experience the object or situation that is causing him or her fear.
				For example, if he or she is afraid of a certain person, don’t let that person try to pick
				them up and hold them; this will only make them more fearfully of that person.
			</li>
			<li>
				Be cautious in handling your cat when he or she is frightened. They may accidentally direct
				their aggression toward you.
			</li>
		</ul>
	</SubSection>
</Section>

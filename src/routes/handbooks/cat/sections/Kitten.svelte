<Section title="Understanding your kittens behavior and development">
	<p>
		The apple doesn’t fall far from the tree, especially for cats. The fact is, well-socialized cats
		are more likely to have well-socialized kittens. Kittens often mirror their mothers’ calm or
		fearful attitudes toward people; this is a normal part of their socialization. But you can play
		a vital role too by petting, talking, and playing with kitty to help him or her develop food
		“people skills.”
	</p>
	<p>
		Kittens are usually weaned at 6-7 weeks but may continue to suckle for comfort as their mother
		gradually leaves them for longer periods of time. Orphaned kittens or those weaned too soon are
		more likely to exhibit inappropriate suckling behaviors later in life, such as sucking on
		blankets, pillows, or your arm. Ideally, kittens should stay with their littermate (or other
		“role-model” cats) for at least 12 weeks.
	</p>
	<p>
		Kittens orphaned or separated from their mothers or littermate too early often fail to develop
		appropriate social skills, such as leaning how to send and receive signals, what an “inhibited
		bite” (acceptable mouthing pressure) means, how far to go in play-wrestling, and so forth. Play
		is important for kittens because it increase their physical coordination and social skills and
		helps in learning limits. By interacting with their mothers and littermate, kittens explore the
		ranking process (“who’s in charge”) and also lean how to be a cat.
	</p>
	<p>
		Kittens who are gently handled by people 15-49 minutes a day during the first 7 weeks are more
		likely to develop larger brains. They’re more exploratory, more playful, and better leaners.
		Skills not acquired during the first 8 weeks may be lost forever. While these stages are
		important and fairly consistent, a cat’s mind remains receptive t new experiences and lessons
		well beyond kitten hood. Most cats are still kittens in mind and body through the first 2 years
		of life.
	</p>
	<SubSection title="0-2 weeks: neonatal period">
		<ul class="list-disc pl-6">
			<li>Kitten learns to orient toward sound.</li>
			<li>Eyes begin opening; they are usually open by 2 weeks of age.</li>
			<li>
				Competition for rank and territory begins. Separation from mother and litter mates at this
				point can lead to poor learning skills and aggression towards people and other pets.
			</li>
		</ul>
	</SubSection>
	<SubSection title="2-7 weeks: socialization period">
		<ul class="list-disc pl-6">
			<li>
				By the third week, sense of smell is well developed and kittens can see well though to find
				their mothers.
			</li>
			<li>
				By the fourth week, sense of smell is fully mature and sense of hearing is well developed.
				Kitten start to interact with litter-mates and can walk fairly well. Teeth start to come in.
			</li>
			<li>
				By fifth week, eyesight is fully mature, and a kitten can right herself, run, place their
				feet precisely, avoid obstacles, stalk and pounce, and catch “prey” with their eyes.
			</li>
			<li>Kitten starts to groom themselves and others.</li>
			<li>
				By the sixth and seventh weeks, kitten begins to develop adult sleeping patterns, motor
				skills, and social interaction abilities.
			</li>
		</ul>
	</SubSection>
	<SubSection title="7-14 weeks: most active play period">
		<ul class="list-disc pl-6">
			<li>
				Social and object play increases kitten’s physical coordination and social skills. Most
				learning is by observation, preferably of kitten’s mother.
			</li>
			<li>Social play includes belly-ups, hugging, ambushing, and licking.</li>
			<li>Object play includes scooping, tossing, pawing, mouthing, and holding.</li>
			<li>Combined social/object play includes tail chasing, pouncing, leaping and dancing.</li>
		</ul>
	</SubSection>
	<SubSection title="3-6 months: ranking period">
		<ul class="list-disc pl-6">
			<li>
				Kittens are most influenced by their “litter,” which may now include playmates of other
				species.
			</li>
			<li>
				Kitten begins to see and use ranking (dominance or submission) within household, including
				humans.
			</li>
		</ul>
	</SubSection>
	<SubSection title="6-18 months: adolescence">
		<ul class="list-disc pl-6">
			<li>Kitten increases exploration of dominance, including challenging humans.</li>
			<li>
				If not spayed or neutered, kitten experiences beginning of sexual behavior. (Spaying or
				neutering you kitten at an early age will increase the health benefits of the surgery and
				increase their lifespan).
			</li>
		</ul>
	</SubSection>
</Section>
